/// Escala de espaciado estándar de Avanzza.
class AppSpacing {
  /// 4 dp — espaciado mínimo (entre ícono y label, separadores finos)
  static const double xs = 4;

  /// 8 dp — espaciado pequeño (entre elementos internos de un componente)
  static const double sm = 8;

  /// 16 dp — espaciado estándar (padding interno de cards, inputs)
  static const double md = 16;

  /// 24 dp — espaciado grande (entre secciones de una misma pantalla)
  static const double lg = 24;

  /// 32 dp — espaciado extragrande (márgenes laterales en onboarding, gaps)
  static const double xl = 32;

  /// 48 dp — espaciado jumbo (separación entre bloques visuales mayores)
  static const double xxl = 48;
}
